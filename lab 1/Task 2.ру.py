import random  # Импортируем модуль random для генерации случайных чисел

# Генерируем случайное число n в диапазоне от 1 до 10
n = random.randint(1, 10)

print(f"Сгенерированное число n: {n}")  # Выводим сгенерированное число

print("Числа от 1 до n:")

# Цикл для вывода всех чисел от 1 до n
for i in range(1, n + 1):
    print(i)  

print("Квадраты чисел от 1 до n:")

# Цикл для вывода квадратов всех чисел от 1 до n
for i in range(1, n + 1):
    print(i ** 2)  

total_sum = sum(range(1, n + 1))

print("Сумма всех чисел от 1 до n:")
print(total_sum)